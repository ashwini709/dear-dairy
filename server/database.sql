CREATE DATABASE dairy;
USE dairy;
SHOW TABLES;
CREATE TABLE entry (id BIGINT(20) NOT NULL AUTO_INCREMENT, title VARCHAR(45), text VARCHAR(200), created_at TIMESTAMP NULL, updated_at TIMESTAMP NULL, PRIMARY KEY (id));
SHOW COLUMNS FROM entry;


CREATE TABLE user (id INT(11) NOT NULL AUTO_INCREMENT, name VARCHAR(255) NOT NULL, email VARCHAR(255) NOT NULL, password VARCHAR(255) NOT NULL, created_at TIMESTAMP NULL, updated_at TIMESTAMP NULL, PRIMARY KEY (id));
SHOW COLUMNS FROM user;
